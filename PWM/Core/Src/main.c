int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_I2C1_Init();
  MX_I2S3_Init();
  MX_SPI1_Init();
  MX_USB_HOST_Init();
  MX_TIM9_Init();
  /* USER CODE BEGIN 2 */
   // --- SİSTEM BAŞLANGIÇ AYARLARI ---
   Lcd_Init();
   Lcd_String("DC MOTOR KONTROL");
   HAL_Delay(1000);
   Lcd_Clear();

   // PWM Sinyalini Başlat (TIM9, Kanal 1)
   HAL_TIM_PWM_Start(&htim9, TIM_CHANNEL_1);

   // Varsayılan olarak motoru İleri yönde hazırla
   Motor_Ileri();
   /* USER CODE END 2 */

  /* Infinite loop */
   /* USER CODE BEGIN WHILE */
   while (1)
   {
   // --- 1. BUTON OKUMA VE KONTROL ---

   // HIZ ARTIRMA (PD14 - Sağ Tuş)
   if (HAL_GPIO_ReadPin(GPIOD, GPIO_PIN_14) == GPIO_PIN_SET)
   {
   if (hiz_pwm < 200) hiz_pwm += 20; // Hızı artır (Adım: 20)
   HAL_Delay(200); // Buton arkını önlemek için bekleme
   }
   // HIZ AZALTMA (PD12 - Sol Tuş)
   if (HAL_GPIO_ReadPin(GPIOD, GPIO_PIN_12) == GPIO_PIN_SET)
   {
   if (hiz_pwm > 0) hiz_pwm -= 20; // Hızı azalt
   HAL_Delay(200);
   }
   // YÖN DEĞİŞTİRME (PD13 - Yukarı Tuş)
   if (HAL_GPIO_ReadPin(GPIOD, GPIO_PIN_13) == GPIO_PIN_SET)
   {
   yon = !yon; // Yönü tersine çevir (Toggle)

   if (yon == 1) Motor_Ileri();
   else Motor_Geri();

   HAL_Delay(300); // Yön değişimi için bekleme süresi
   }
   // --- 2. MOTOR SÜRME VE EKRAN GÜNCELLEME ---
   // Hesaplanan PWM değerini Timer'ın CCR registerına yaz
   __HAL_TIM_SET_COMPARE(&htim9, TIM_CHANNEL_1, hiz_pwm);
   // PWM değerini (0-200) Yüzdeye (%0-100) çevir
   hiz_yuzde = (hiz_pwm * 100) / 200;
   // LCD Ekrana Durumu Yaz
   Lcd_Set_Cursor(0, 0);
   if (yon == 1) Lcd_String("YON: ILERI (CW) ");
   else Lcd_String("YON: GERI (CCW) ");
   Lcd_Set_Cursor(1, 0);

   // HATA BURADAYDI: "kullanılır" kelimesi alt satıra düşmüştü, yanına çektik.
   sprintf(lcd_buffer, "HIZ: %%%d ", hiz_yuzde); // "%" karakteri için "%%" kullanılır

   Lcd_String(lcd_buffer);
   HAL_Delay(50); // Döngü kararlılığı için kısa bekleme

   /* USER CODE END WHILE */
   }

    MX_USB_HOST_Process();

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */

